    private void ProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProcurarActionPerformed
        // TODO add your handling code here:
        final JFileChooser fc = new JFileChooser();
        String caminho = null;
        String usuarioBancoDeDados = "SYSDBA";

        try {
            int res;

            res = fc.showOpenDialog(null);
            FileOutputStream file = new FileOutputStream(new File("CaminhoBanco.txt"));
            FileOutputStream fileUserBD = new FileOutputStream(new File("usuarioBancoDeDados.txt"));

            if (res == JFileChooser.APPROVE_OPTION) {
                caminho = "jdbc:firebirdsql://localhost:3050/"+fc.getSelectedFile().getAbsolutePath();
            }
            file.write(caminho.getBytes());
            fileUserBD.write(usuarioBancoDeDados.getBytes());

            caminho_Banco_de_Dados.setText(caminho);

        } catch (IOException ex) {
            Logger.getLogger(Configuracoes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ProcurarActionPerformed

