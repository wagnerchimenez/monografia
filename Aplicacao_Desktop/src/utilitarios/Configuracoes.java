    private void ConfirmaRemotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmaRemotoActionPerformed
        // TODO add your handling code here:
        conexao.usuario = "glasswindow";

        final JFileChooser fc = new JFileChooser();
        String caminho = caminho_Banco_de_DadosRemoto.getText().trim();
        String usuarioBancoDeDados = "glasswindow";

        try {
            FileOutputStream file = new FileOutputStream(new File("CaminhoBanco.txt"));
            FileOutputStream fileUserBD = new FileOutputStream(new File("usuarioBancoDeDados.txt"));

            file.write(caminho.getBytes());
            fileUserBD.write(usuarioBancoDeDados.getBytes());

        } catch (IOException ex) {
            Logger.getLogger(Configuracoes.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            // TODO add your handling code here:
            Global.conection = new conexao(caminho_Banco_de_DadosRemoto.getText().trim(), usuarioRemoto.getText().trim(), senhaRemoto.getText().trim());
        } catch (IOException ex) {
            Logger.getLogger(Configuracoes.class.getName()).log(Level.SEVERE, null, ex);
        }

        dispose();


    }//GEN-LAST:event_ConfirmaRemotoActionPerformed

